#!/usr/bin/env node
// Throwaway: convert ~/Code/hickman-xmen/{lookup,applinks}.json into
// lib/comics-data.ts. Re-run by hand if the source list changes.
import fs from "node:fs";
import path from "node:path";
import os from "node:os";

const SRC = path.join(os.homedir(), "Code", "hickman-xmen");
const lookup = JSON.parse(fs.readFileSync(path.join(SRC, "lookup.json"), "utf8"));
const applinks = JSON.parse(fs.readFileSync(path.join(SRC, "applinks.json"), "utf8"));

const issues = lookup
  .map((row) => {
    const digital = row.slug ? applinks[row.slug] : null;
    if (!digital || !row.id || !row.slug) return null;
    return {
      id: digital,
      title: row.title,
      digitalBookId: digital,
      marvelIssueId: row.id,
      slug: row.slug,
    };
  })
  .filter(Boolean);

const out = `// AUTO-GENERATED by scripts/generate-comics-data.mjs.
// Source: ~/Code/hickman-xmen/{lookup,applinks}.json
// Re-run the script if the underlying reading order changes.

export interface ComicIssue {
  id: string;
  title: string;
  digitalBookId: string;
  marvelIssueId: string;
  slug: string;
}

export interface Storyline {
  slug: string;
  title: string;
  description?: string;
  issues: ComicIssue[];
}

export const STORYLINES: Storyline[] = [
  {
    slug: "hickman-x-men",
    title: "Jonathan Hickman's X-Men",
    description:
      "House of X / Powers of X through the X of Swords crossover. ${issues.length} issues.",
    issues: ${JSON.stringify(issues, null, 6).replace(/\n/g, "\n    ")},
  },
];

export function getStoryline(slug: string): Storyline | undefined {
  return STORYLINES.find((s) => s.slug === slug);
}
`;

const dest = path.join(process.cwd(), "lib", "comics-data.ts");
fs.writeFileSync(dest, out);
console.log(`Wrote ${dest} (${issues.length} issues)`);
